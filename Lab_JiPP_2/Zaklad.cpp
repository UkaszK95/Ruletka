#include "Zaklad.h"
#include <iostream>
using namespace std;

Zaklad::Zaklad(){ //konstruktor domyslny
	kwota = 0;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Zaklad::Zaklad(double kwota, Rodzajzakladu * rodzaj){  
	rodzajZakladu = rodzaj;
	this->kwota = kwota;
}

Rodzajzakladu *Zaklad::GetRodzaj()
{
	return rodzajZakladu;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double Zaklad::GetKwota()
{
	return kwota;
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool Zaklad::CzyWygrana(WynikRuletki &wynik)
{
	if(*rodzajZakladu == wynik)
	{
		return true;
	}
	return false;
}
void Zaklad::Wypisz()
{
	rodzajZakladu->Wypisz();
}